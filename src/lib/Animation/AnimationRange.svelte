				// the starting frame of the animation
				

				export let to = undefined // [number] * required
				// the ending frame of the animation
				

				// ================ CLASS INSTANCE ================

				export let instance = browser ? new BB.AnimationRange(name,from,to) : {}

				// ================ ACCESSORS ================

				

				// ================ PROPERTIES ================

				

				// ================ METHODS ================

				
