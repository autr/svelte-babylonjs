							$bbIniting = !Array.from($bbLookup.values()).every(v => v)
							DEBUG('ðŸŒ¿ inited')
							onInited(instance)

							if (!$bbIniting) {

												if (name != undefined) instance.name = name?.instance || name
				name = instance.name
				if (from != undefined) instance.from = from?.instance || from
				from = instance.from
				if (to != undefined) instance.to = to?.instance || to
				to = instance.to

								// ================ AFRAME PARENT ================

